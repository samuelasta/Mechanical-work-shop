package co.uniquindio.edu.model.enums;

public enum TipoServicio {
    MANTENIMIENTO_PREVENTIVO,   // cambio de aceite, filtros, revisión general
    MANTENIMIENTO_CORRECTIVO,   // reparación de fallas mecánicas o eléctricas
    DIAGNOSTICO_ELECTRONICO,    // escaneo del sistema computarizado del vehículo
    MECANICA_GENERAL,           // motor, caja, suspensión, frenos, etc.
    ELECTRICIDAD_AUTOMOTRIZ,    // alternador, batería, luces, cableado
    LATONERIA,                  // reparación de golpes o abolladuras
    PINTURA,                    // pintura parcial o total del vehículo
    ALINEACION_BALANCEO,        // corrección de dirección y balanceo de llantas
    CAMBIO_DE_ACEITE,           // lubricación y reemplazo de aceite/filtros
    LAVADO_DETALLADO,           // limpieza interior y exterior a profundidad
    REVISION_TECNOMECANICA,     // revisión antes de inspección oficial
    INSTALACION_ACCESORIOS,     // alarmas, sensores, luces, vidrios eléctricos
    REPARACION_AIRE_ACONDICIONADO, // gas, compresor, ventilación
    SISTEMA_DE_FRENOS,          // revisión o cambio de pastillas y discos
    SUSPENSION_Y_DIRECCION
}
